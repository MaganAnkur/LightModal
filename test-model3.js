// Refined YOLO decoding and NMS logic for React Native

const sigmoid = x => 1 / (1 + Math.exp(-x));
const softmax = scores => {
  const max = Math.max(...scores);
  const exps = scores.map(v => Math.exp(v - max));
  const sum = exps.reduce((a, b) => a + b);
  return exps.map(v => v / sum);
};

// Anchors used during training (normalized), scaled to 512 input and 13x13 grid
const anchors = [
  [0.573, 0.677],
  [1.87, 2.06],
  [3.34, 5.47],
  [7.88, 3.53],
  [9.77, 9.17],
];

function decodeYoloOutput(
  flatOutput,
  {
    inputSize = 512,
    gridSize = 13,
    numAnchors = 5,
    numClasses = 4,
    confidenceThreshold = 0.1,
  } = {},
) {
  const boxes = [];
  const valuesPerBox = 5 + numClasses;
  const stride = numAnchors * valuesPerBox;

  const scaledAnchors = anchors.map(([w, h]) => [
    w * (inputSize / gridSize),
    h * (inputSize / gridSize),
  ]);

  for (let cy = 0; cy < gridSize; cy++) {
    for (let cx = 0; cx < gridSize; cx++) {
      for (let a = 0; a < numAnchors; a++) {
        const offset = (cy * gridSize + cx) * stride + a * valuesPerBox;
        const tx = flatOutput[offset];
        const ty = flatOutput[offset + 1];
        const tw = flatOutput[offset + 2];
        const th = flatOutput[offset + 3];
        const objectness = sigmoid(flatOutput[offset + 4]);

        const classScores = flatOutput
          .slice(offset + 5, offset + 5 + numClasses)
          .map(sigmoid);
        const scores = classScores.map(score => score * objectness);
        const confidence = Math.max(...scores);
        const classIndex = scores.indexOf(confidence);

        if (confidence > confidenceThreshold) {
          const [anchorW, anchorH] = scaledAnchors[a];
          const gridUnit = inputSize / gridSize;

          const bx = (sigmoid(tx) + cx) * gridUnit;
          const by = (sigmoid(ty) + cy) * gridUnit;
          const bw = anchorW * Math.exp(tw);
          const bh = anchorH * Math.exp(th);

          // Normalize to [0, 1]
          boxes.push({
            x: Math.max(0, Math.min(1, (bx - bw / 2) / inputSize)),
            y: Math.max(0, Math.min(1, (by - bh / 2) / inputSize)),
            width: Math.min(1, bw / inputSize),
            height: Math.min(1, bh / inputSize),
            confidence,
            classIndex,
          });
        }
      }
    }
  }
  return boxes;
}

function nonMaxSuppression(boxes, iouThreshold = 0.45) {
  const iou = (boxA, boxB) => {
    const xA = Math.max(boxA.x, boxB.x);
    const yA = Math.max(boxA.y, boxB.y);
    const xB = Math.min(boxA.x + boxA.width, boxB.x + boxB.width);
    const yB = Math.min(boxA.y + boxA.height, boxB.y + boxB.height);
    const interArea = Math.max(0, xB - xA) * Math.max(0, yB - yA);
    const boxAArea = boxA.width * boxA.height;
    const boxBArea = boxB.width * boxB.height;
    return interArea / (boxAArea + boxBArea - interArea + 1e-6);
  };

  const result = [];
  const byClass = {};
  for (const box of boxes) {
    if (!byClass[box.classIndex]) byClass[box.classIndex] = [];
    byClass[box.classIndex].push(box);
  }

  for (const classBoxes of Object.values(byClass)) {
    const sorted = classBoxes.sort((a, b) => b.confidence - a.confidence);
    while (sorted.length > 0) {
      const best = sorted.shift();
      result.push(best);
      for (let i = sorted.length - 1; i >= 0; i--) {
        if (iou(best, sorted[i]) > iouThreshold) {
          sorted.splice(i, 1);
        }
      }
    }
  }

  return result;
}

function postprocess(
  rawBoxes,
  labels,
  probThreshold = 0.1,
  maxDetections = 20,
) {
  const selected = rawBoxes
    .filter(b => b.confidence > probThreshold)
    .sort((a, b) => b.confidence - a.confidence)
    .slice(0, maxDetections);

  const final = nonMaxSuppression(selected);

  return final.map(b => ({
    probability: parseFloat(b.confidence.toFixed(6)),
    tagId: b.classIndex,
    tagName: labels[b.classIndex],
    boundingBox: {
      left: parseFloat(b.x.toFixed(6)),
      top: parseFloat(b.y.toFixed(6)),
      width: parseFloat(b.width.toFixed(6)),
      height: parseFloat(b.height.toFixed(6)),
    },
  }));
}

