function decodeYoloOutput(
  flatOutput,
  {
    inputSize = 416,
    gridSize = 13,
    numAnchors = 5,
    numClasses = 4,
    confidenceThreshold = 0.03,
  } = {},
) {
  const boxes = [];
  const valuesPerBox = 5 + numClasses;
  const stride = numAnchors * valuesPerBox;

  for (let cy = 0; cy < gridSize; cy++) {
    for (let cx = 0; cx < gridSize; cx++) {
      for (let anchorIdx = 0; anchorIdx < numAnchors; anchorIdx++) {
        const offset = (cy * gridSize + cx) * stride + anchorIdx * valuesPerBox;

        const tx = flatOutput[offset];
        const ty = flatOutput[offset + 1];
        const tw = flatOutput[offset + 2];
        const th = flatOutput[offset + 3];
        const objectness = sigmoid(flatOutput[offset + 4]);

        const classScores = [];
        for (let i = 0; i < numClasses; i++) {
          classScores.push(sigmoid(flatOutput[offset + 5 + i]));
        }

        const scores = classScores.map(s => s * objectness);
        const confidence = Math.max(...scores);
        const classIndex = scores.indexOf(confidence);

        if (confidence > confidenceThreshold) {
          const anchor = anchors[anchorIdx];
          const gridUnit = inputSize / gridSize;

          const bx = (sigmoid(tx) + cx) * gridUnit;
          const by = (sigmoid(ty) + cy) * gridUnit;
          const bw = anchor[0] * Math.exp(tw);
          const bh = anchor[1] * Math.exp(th);

          const x1 = bx - bw / 2;
          const y1 = by - bh / 2;
          const x2 = bx + bw / 2;
          const y2 = by + bh / 2;

          boxes.push({
            x: x1,
            y: y1,
            width: bw,
            height: bh,
            confidence,
            classIndex,
          });
        }
      }
    }
  }

  return boxes;
}

function iou(boxA, boxB) {
  const xA = Math.max(boxA.x, boxB.x);
  const yA = Math.max(boxA.y, boxB.y);
  const xB = Math.min(boxA.x + boxA.width, boxB.x + boxB.width);
  const yB = Math.min(boxA.y + boxA.height, boxB.y + boxB.height);

  const interArea = Math.max(0, xB - xA) * Math.max(0, yB - yA);
  const boxAArea = boxA.width * boxA.height;
  const boxBArea = boxB.width * boxB.height;

  return interArea / (boxAArea + boxBArea - interArea);
}

function nonMaxSuppression(boxes, iouThreshold = 0.5) {
  const result = [];

  // Group boxes by classIndex
  const byClass = {};
  for (const box of boxes) {
    const cls = box.classIndex;
    if (!byClass[cls]) byClass[cls] = [];
    byClass[cls].push(box);
  }

  for (const classIndex in byClass) {
    const classBoxes = byClass[classIndex].sort(
      (a, b) => b.confidence - a.confidence,
    );

    while (classBoxes.length > 0) {
      const best = classBoxes.shift();
      result.push(best);

      for (let i = classBoxes.length - 1; i >= 0; i--) {
        const iouVal = iou(best, classBoxes[i]);
        if (iouVal > iouThreshold) {
          classBoxes.splice(i, 1);
        }
      }
    }
  }

  return result;
}

